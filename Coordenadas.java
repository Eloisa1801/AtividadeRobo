public class Coordenadas {

    double x, y;

    public Coordenadas(){}

    public Coordenadas(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }
    public void setY(Double y) {
        this.y = y;
    }
    
}
